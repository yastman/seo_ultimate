<h2>Критические ошибки</h2>

<strong>Нанесение на горячие диски</strong> — химия для дисков высыхает мгновенно, оставляя несмываемые разводы. Суппорта Brembo могут побледнеть от химического ожога — яркий лак станет матовым.

<strong>Кислота на хроме или PCCB</strong> — необратимые матовые пятна. Карбон-керамические тормоза стоят сотни тысяч — их убивает любая агрессивная химия.

<strong>Кислота на дисках со сколами</strong> — если есть «бордюрка», кислота проникает ПОД лак через скол и поднимает покрытие изнутри. Лак начнёт слезать паутинкой за сезон.

<strong>Попадание щёлочи на резину</strong> — вызывает Tire Blooming (порыжение шин). Щёлочь вымывает антиозонанты из резины. Смывайте состав с шин немедленно.

<strong>Flash Rust после мойки</strong> — если помыли диски Iron Remover и оставили машину мокрой на ночь, тормоза могут «прикипеть». После мойки проедьте 100 метров и пару раз резко затормозите.

<strong>Использование средств с HF</strong> — плавиковая кислота опасна для жизни. Проверяйте состав перед покупкой.

