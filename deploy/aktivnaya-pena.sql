<h2>FAQ: Ответы эксперта</h2>

<h3>Пена стекает как вода и не держится. Что делать?</h3>

<p>Чаще дело не в химии, а в оборудовании. Дешёвые пенокомплекты имеют плохую пенную \"таблетку\" (сеточку). Попробуйте увеличить концентрацию (1:5 вместо 1:10). Если не помогло — замените таблетку в пенообразователе. Используйте тёплую воду — она лучше взбивает пену.</p>

<h3>Почему после пены машина всё равно грязная (серый налёт)?</h3>

<p>Активная пена — бесконтактная подготовка (Pre-wash). Убирает песок и крупные частицы (80% грязи). Тонкую статическую плёнку (Traffic Film) уберёт только механическое трение губкой во второй фазе. Не пытайтесь отмыть статику пеной, увеличивая концентрацию — сожжёте хром, но налёт останется.</p>

<h3>Snow Foam или Pre-Wash (TFR)? В чём разница?</h3>

<strong>Snow Foam</strong> — густая \"шапка\", обычно pH-нейтральная. Работает слабее, но безопасна. Идеальна для летней пыли. <strong>TFR (Traffic Film Remover)</strong> — жидкая эмульсия, почти без пены. Обычно щелочная. Выглядит скучно, но \"съедает\" зимнюю грязь и реагенты в разы лучше.

<h3>Можно ли щелочную пену на машине с керамикой?</h3>

<p>Да, но с умом. Зимой нейтральная пена (pH 7) не смоет масляную плёнку. Керамика выдержит щёлочь (pH 11–12), если не давать ей высыхать. Используйте щёлочь только когда машина реально грязная. Летом вернитесь к нейтральным составам.</p>

<h3>Пена оставила белые пятна на хроме. Это полируется?</h3>

<p>Это химический ожог щёлочью (Alkaline Burn) — случается на горячем молдинге или при высыхании. Лёгкие пятна убираются финишной полиролью для хрома. Белые потёки на Shadow Line (анодированный алюминий) часто необратимы — придётся перекрашивать или клеить плёнку. <strong>Всегда остужайте кузов летом перед нанесением!</strong></p>'
