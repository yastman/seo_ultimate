<h3>Частые ошибки</h3>

<p><strong>Тёрли сухой тряпкой</strong> — битум размазывается, абразивные частицы царапают лак. Сначала растворите.</p>

<p><strong>Оставили на солнце</strong> — растворитель высыхает пятнами. Работайте в тени, смывайте за 2–5 минут.</p>

<p><strong>Горячий кузов</strong> — состав испаряется быстрее, чем растворяет. Дайте остыть.</p>

<p><strong>Не восстановили защиту</strong> — после обработки воск снимается полностью. Нанесите быстрый воск.</p>

<p><strong>Сольвент на старых фарах</strong> — поликарбонат с изношенным UV-слоем впитывает растворитель. Результат — микротрещины (crazing). Для фар — только цитрусовые.</p>

<p><strong>Сольвент на чёрном пластике</strong> — нефтяные составы оставляют белёсые разводы на неокрашенном структурном пластике (арки, бамперы). Если попали — сразу смойте водой и протрите чернителем.</p>

