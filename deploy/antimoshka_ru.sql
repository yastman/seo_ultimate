<h3>Частые ошибки</h3>

<p><strong>Shadow Line</strong> — не наносите щелочь на горячие молдинги. <strong>Всегда остужайте деталь водой перед нанесением!</strong> Если нанести щелочь на горячий алюминий (>30°C), ожог останется навсегда.</p>

<p><strong>Бачок омывателя</strong> — не лейте спрей в бачок. Пена завоздушит насос, ПАВ забьёт форсунки.</p>

