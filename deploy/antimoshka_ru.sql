<h2>FAQ: Ответы эксперта</h2>

<h3>Антимошка убьёт мою керамику или воск?</h3>
<p>Качественные цитрусовые составы безопасны. Щелочные средства (pH > 11) значительно ослабляют натуральный воск. Если у вас дорогая керамика, используйте только pH-сбалансированные очистители.</p>

<h3>Можно ли наносить на горячий капот?</h3>
<p><strong>Категорически НЕТ.</strong> Вода испарится за секунды, оставив концентрированную щелочь. Это гарантированный химический ожог лака и помутнение хрома. Сначала остудите капот водой!</p>

<h3>Безопасно ли это для фар?</h3>
<p>Водные составы безопасны. Опасайтесь средств с резким запахом растворителя или керосина — они могут вызвать микротрещины («паутинку») на поликарбонате.</p>

<h3>Можно ли залить средство в бачок омывателя?</h3>
<p>Нет, если это не спецконцентрат (Washer Additive). Обычный спрей дает густую пену, которая завоздушит насос и забьет форсунки.</p>

<h3>Помутнели молдинги Shadow Line. Что делать?</h3>
<p>Это химический ожог щелочью. К сожалению, это часто не отмывается. Попробуйте финишную полироль для металла. Если не помогло — только замена или оклейка пленкой (антихром).</p>

<h3>Чем отличается от APC (универсального очистителя)?</h3>
<p>APC — это просто мыльная щелочь. Антимошка содержит ферменты или специальные растворители, которые расщепляют именно белок и хитин. Это как мыть жирную сковородку просто водой или средством «Антижир».</p>'
