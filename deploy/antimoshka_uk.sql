<h2>FAQ: Відповіді експерта</h2>

<h3>Антимошка зіпсує мою кераміку чи віск?</h3>
<p>Якісні цитрусові склади безпечні. Лужні засоби (pH > 11) значно послаблюють натуральний віск. Якщо у вас дорога кераміка, використовуйте тільки pH-збалансовані очищувачі.</p>

<h3>Чи можна наносити на гарячий капот?</h3>
<p><strong>Категорично НІ.</strong> Вода випарується за секунди, залишивши концентровану луг. Це гарантований хімічний опік лаку та помутніння хрому. Спочатку охолодіть капот водою!</p>

<h3>Чи безпечно це для фар?</h3>
<p>Водні склади безпечні. Остерігайтеся засобів із різким запахом розчинника або гасу — вони можуть викликати мікротріщини («павутинку») на полікарбонаті.</p>

<h3>Чи можна залити засіб у бачок омивача?</h3>
<p>Ні, якщо це не спецконцентрат (Washer Additive). Звичайний спрей дає густу піну, яка завоздушить насос і заб''є форсунки.</p>

<h3>Помутніли молдинги Shadow Line. Що робити?</h3>
<p>Це хімічний опік лугом. На жаль, це часто не відмивається. Спробуйте фінішну полірол для металу. Якщо не допомогло — тільки заміна або оклейка плівкою (антихром).</p>

<h3>Чим відрізняється від APC (універсального очищувача)?</h3>
<p>APC — це просто мильна луг. Антимошка містить ферменти або спеціальні розчинники, які розщеплюють саме білок і хітин. Це як мити жирну сковорідку просто водою або засобом «Антижир».</p>'
