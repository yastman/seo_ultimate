<h2>Виды средств (что за чем)</h2>

<h3>1) Индикаторные составы (iron remover)</h3>

<p>Это <strong>химия для чистки дисков</strong>, которая реагирует с железом: вы увидите изменение цвета — значит, металл “отпускает”.</p>

<h3>2) Кислотные очистители</h3>

<p>Тяжёлая артиллерия для застарелого налёта/минералов. Работают быстро, но требуют аккуратности и test spot.</p>

<h3>3) Бескислотные / щадящие</h3>

<p>Хороши как <strong>средство для мойки дисков</strong> на регулярной основе: снимают дорожную грязь и жир, а с металлом работают мягче.</p>

<h2>Как мыть диски (пошагово)</h2>

<ol>
<li><strong>Остудите тормоза.</strong></li>
<li>Сбейте песок водой.</li>
<li>Нанесите <strong>очиститель колесных дисков</strong> на холодную поверхность.</li>
<li>Дайте поработать (по инструкции) и не пересушивайте.</li>
<li>При необходимости — кисть/щётка для внутренней полки.</li>
<li>Тщательно смойте и проверьте колодцы болтов.</li>
</ol>

<h2>Концентрат или готовый состав (Evergreen Math)</h2>

<ul>
<li>Готовая <strong>жидкость для чистки дисков</strong> удобна: открыл → нанёс → смыл.</li>
<li>Концентрат выгоднее на дистанции: можно разводить под задачу, и из 1 литра часто получается несколько литров рабочего раствора (пропорции — по этикетке).</li>
</ul>

